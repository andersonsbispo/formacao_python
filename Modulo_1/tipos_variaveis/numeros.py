#Inteiro (int)
numero_inteiro = 3
print('Inteiro =', numero_inteiro)

#Real com ponto flutuante (float)
numero_real = 3.8
print('Real com ponto flutuante =', numero_real)

#type
print('Tipo da variável inteiro =', type(numero_inteiro))
print('Tipo da variável real =', type(numero_real))

#soma +
soma = 1 + 1
print('1 + 1 =', soma)

#subtração -
subtracao = 1 - 1
print('1 - 1 =', subtracao)

#multiplicação *
multiplicacao = 2 * 2
print('2 x 2 =', multiplicacao)

#divisão (float) /
divisao1 = 5 / 2
print('5 / 2 =', divisao1)
print('Tipo da variavel do resultado da divisao =', type(divisao1))

#divisão (int) //
divisao2 = 5 // 2
print('5 // 2 =', divisao2)
print('Tipo da variavel do resultado da divisao =', type(divisao2))

#Convertendo float para int()
print('Valor em inteiro', int(divisao1))

#Convertendo int em float()
print('Valor em inteiro', float(divisao2))

#módulo %
modulo = 5 % 2
print('5 % 2 =', modulo)